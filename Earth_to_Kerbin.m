function [h,t_e,p_e,rho_e,c] = Earth_to_Kerbin(z)
    R = 287;
    gamma = 1.4;
    h = (7963.75*z)/(6371+1.25*z);
    if h <= 11
        t_e = 288.15 - 6.5*h;
        p_e = 101325*(288.15/t_e)^(34.1632/-6.5);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 11 && h <= 20
        t_e = 216.65;
        p_e = 22632.06*exp((-34.1632*(h-11))/216.65);
        rho_e = p_e/(R*t_e); 
        c = sqrt(gamma*R*t_e);
    elseif h > 20 && h <= 32
        t_e = 196.65 + h;
        p_e = 5474.889*(216.65/t_e)^(34.1632);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 32 && h <= 47
        t_e = 139.05 + 2.8*h;
        p_e = 860.0197*(228.65/t_e)^(34.1632/2.8);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 47 && h <= 51
        t_e = 270.65;
        p_e = 110.9063*exp((-34.1632*(h-47))/270.65);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 51 && h <= 71
        t_e = 413.45-2.8*h;
        p_e = 66.93887*(270.65/t_e)^(34.1632/-2.8);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 71 && h <= 84.852
        t_e = 356.65 - 2*h;
        p_e = 3.956420*(214.65/t_e)^(34.1632/-2);
        rho_e = p_e/(R*t_e);
        c = sqrt(gamma*R*t_e);
    elseif h > 84.852 && h <= 91
        t_e = 186.8673;
        p_e = 0;
        rho_e = 0;
        c = sqrt(gamma*R*t_e);
    elseif h > 91 && h <= 110
        t_e = 263.1905-76.3232*sqrt(1-((h-91)/-19.9429)^2);
        p_e = 0;
        rho_e = 0;
        c = sqrt(gamma*R*t_e);
    elseif h > 110 && h <= 120
        t_e = 240+12*(h-110);
        p_e = 0;
        rho_e = 0;
        c = sqrt(gamma*R*t_e);
    elseif h > 120 && h <= 1000
        t_e = 1000-640*exp(-0.01875*((h-120)*(6356.766+120)/(6356.766+h)));
        p_e = 0;
        rho_e = 0;
        c = sqrt(gamma*R*t_e);
    elseif h > 1000
        t_e = 1000-640*exp(-0.01875*((1000-120)*(6356.766+120)/(6356.766+1000)));
        p_e = 0;
        rho_e = 0;
        c = sqrt(gamma*R*t_e);
    end
    
    
end